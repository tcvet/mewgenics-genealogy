const DICTS: Record<Lang, Dict> = { en, ru, de, fr, es, pt, zh, ja, ko };

function loadLang(): Lang {
  const saved = localStorage.getItem(LANG_KEY);
  return LANGS.some((l) => l.code === saved) ? (saved as Lang) : 'en';
}

const I18nContext = createContext<{ lang: Lang; setLang: (lang: Lang) => void; t: Dict }>({
  lang: 'en',
  setLang: () => {},
  t: en,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(loadLang);
  useEffect(() => {
    localStorage.setItem(LANG_KEY, lang);
    document.documentElement.lang = lang;
  }, [lang]);
  const value = useMemo(() => ({ lang, setLang, t: DICTS[lang] }), [lang]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
